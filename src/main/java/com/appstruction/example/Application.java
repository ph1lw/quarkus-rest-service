import javax.enterprise.context.ApplicationScoped;

@Readiness
@ApplicationScoped
public class Application implements HealthCheck {

    @Override
    public HealthCheckResponse call() {
        return HealthCheckResponse.up("Quarkus Rest Service is running.");
    }
}
